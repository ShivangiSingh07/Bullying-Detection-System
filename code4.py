# Topic: For Loop in Python
# A for loop is used when we know how many times we want to repeat a task.
# It works with sequences like ranges, lists, or strings.

# Example 1: Printing numbers from 1 to 5
for i in range(1, 6):   # range(1,6) gives numbers 1,2,3,4,5
    print("Iteration:", i)

# Example 2: Printing each character in a string
word = "Python"
for ch in word:
    print("Character:", ch)

# Example 3: Printing squares of numbers
for num in range(1, 6):
    print("Square of", num, "is", num * num)

